# soma entre dois números

n1 = int(input('Digite um número: '))  # inserir primeiro número
n2 = int(input('Digite outro número: '))  # inserir segundo número
sum = n1 + n2  # variável com a soma dos dois números
print('A soma entre {} e {} e igual a {}.'.format(n1, n2, sum)) # mostra o resultado da soma dos dois números
