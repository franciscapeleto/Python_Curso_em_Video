# soma entre dois numeros

n1 = int(input('Digite um numero: '))  # inserir primeiro numero
n2 = int(input('Digite outro numero: '))  # inserir segundo numero
sum = n1 + n2  # variavel com a soma dos dois numeros
print('A soma entre {} e {} e igual a {}.'.format(n1, n2, sum)) # mostra o resultado da soma dos dois numeros
